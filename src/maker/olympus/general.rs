impl RawDecoder for OlympusGeneral {
    fn new(info: ParsedRawInfo) -> Self {
        OlympusGeneral { info }
    }
    fn get_info(&self) -> &ParsedRawInfo {
        &self.info
    }

    fn get_task(only_thumbnail: bool, _model: String) -> ExifTask {
        if only_thumbnail {
            create_rule![tiff {
                0x0112 / orientation
                0x8769 {
                    0x927c / maker_notes {
                        offset + 12 {
                            0x2020 {
                                offset + maker_notes {
                                    0x0101 / preview_image_start
                                    0x0102 / preview_image_len
                                }
                            }
                        }
                    }
                }
            }]
        } else {
            create_rule![tiff {
                0x0112 / orientation
                0x0100 / width
                0x0101 / height
                0x0111 / strip
                0x0117 / strip_len
                0x8769 {
                    0xa302 {
                        u32 + 1 / cfa_pattern
                    }
                    0x927c / maker_notes {
                        offset + 12 {
                            0x2040 {
                                offset + maker_notes {
                                    0x0611 / bps
                                    0x0612 / crop_left
                                    0x0613 / crop_top
                                    0x0614 / crop_width
                                    0x0615 / crop_height
                                    0x0100 {
                                        offset + maker_notes {
                                            u16 + 0 / white_balance_r
                                            u16 + 1 / white_balance_b
                                            u16 + 2 / white_balance_g
                                        }
                                    }
                                    0x0600 {
                                        offset + maker_notes {
                                            u16 + 0 / black_level
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }]
        }
    }
    fn get_crop(&self) -> Option<Crop> {
        let x = self.info.u32("crop_left").ok()?;
        let y = self.info.u32("crop_top").ok()?;
        let width = self.info.u32("crop_width").ok()?;
        let height = self.info.u32("crop_height").ok()?;

        Some(Crop { x, y, width, height })
    }
    fn inner_pre_process(&self, buffer: &[u8]) -> Result<Vec<u16>, DecodingError> {
        let width = self.info.usize("width")?;
        let height = self.info.usize("height")?;
        let strip_offset = self.info.usize("strip")?;
        let strip_len = self.info.usize("strip_len")?;
        let buffer = &buffer[strip_offset..];

        let image = if strip_len >= width * height / 10 * 16 {
            load_12bit_raw(buffer, width, height)?
        } else {
            load_compressed_raw(buffer, width, height)?
        };

        let black_level = self.info.u16("black_level")?;
        let bps_scale = self.get_bps_scale()?;
        Ok(image
            .iter()
            .map(|x| bps_scale.saturating_mul(x.saturating_sub(black_level)))
            .collect())
    }
    fn inner_get_thumbnail<'a>(&self, buffer: &'a [u8]) -> Result<&'a [u8], DecodingError> {
        let base = self.info.usize("maker_notes")?;
        let offset = self.info.usize("preview_image_start")? + base;
        let len = self.info.usize("preview_image_len")?;
        Ok(&buffer[offset..offset + len])
    }
}

#[inline(always)]
fn fast_inc_get<T: Copy>(vec: &[T], index: &mut usize) -> T {
    unsafe {
        let ret = *vec.get_unchecked(*index);
        *index += 1;
        ret
    }
}
#[inline(always)]
fn fast_inc_set<T: Copy>(vec: &mut Vec<T>, index: &mut usize, value: T) {
    unsafe {
        *vec.get_unchecked_mut(*index) = value;
        *index += 1;
    }
}

fn load_compressed_raw(buf: &[u8], width: usize, height: usize) -> Result<Vec<u16>, DecodingError> {
    let mut out = vec![0u16; width * height];

    let mut left: [i32; 2] = [0; 2];
    let mut nw: [i32; 2] = [0; 2];
    let mut pump = BitPumpMSB::new(&buf[7..]);
    let mut set_index = 0;
    let mut get_index = 0;

    for row in 0..height {
        let mut acarry: [[i32; 3]; 2] = [[0; 3]; 2];

        for c in 0..width / 2 {
            let col: usize = c * 2;
            for s in 0..2 {
                // Run twice for odd and even pixels
                let i = if acarry[s][2] < 3 { 2 } else { 0 };
                let mut nbits = 2 + i;
                while ((acarry[s][0] >> (nbits + i)) & 0xffff) > 0 {
                    nbits += 1
                }
                nbits = cmp::min(nbits, 16);
                let b = pump.peek_ibits(15);

                let sign: i32 = (b >> 14) * -1;
                let low: i32 = (b >> 12) & 3;
                let mut high: i32 = BITTABLE[(b & 4095) as usize];

                // Skip bytes used above or read bits
                if high == 12 {
                    pump.consume_bits(15);
                    high = pump.get_ibits(16 - nbits) >> 1;
                } else {
                    pump.consume_bits((high + 4) as u32);
                }

                acarry[s][0] = ((high << nbits) | pump.get_ibits(nbits)) as i32;
                let diff = (acarry[s][0] ^ sign) + acarry[s][1];
                acarry[s][1] = (diff * 3 + acarry[s][1]) >> 5;
                acarry[s][2] = if acarry[s][0] > 16 { 0 } else { acarry[s][2] + 1 };

                let v = if row < 2 || col < 2 {
                    // We're in a border, special care is needed
                    let pred = if row < 2 && col < 2 {
                        // We're in the top left corner
                        0
                    } else if row < 2 {
                        // We're going along the top border
                        left[s]
                    } else {
                        // col < 2, we're at the start of a line
                        nw[s] = fast_inc_get(&out, &mut get_index) as i32;
                        nw[s]
                    };
                    left[s] = pred + ((diff << 2) | low);
                    left[s] as u16
                } else {
                    let up: i32 = fast_inc_get(&out, &mut get_index) as i32;
                    let left_minus_nw: i32 = left[s] - nw[s];
                    let up_minus_nw: i32 = up - nw[s];
                    // Check if sign is different, and one is not zero
                    let pred = if left_minus_nw * up_minus_nw < 0 {
                        if left_minus_nw.abs() > 32 || up_minus_nw.abs() > 32 {
                            left[s] + up_minus_nw
                        } else {
                            (left[s] + up) >> 1
                        }
                    } else {
                        if left_minus_nw.abs() > up_minus_nw.abs() {
                            left[s]
                        } else {
                            up
                        }
                    };

                    left[s] = pred + ((diff << 2) | low);
                    nw[s] = up;
                    left[s] as u16
                };
                fast_inc_set(&mut out, &mut set_index, v);
            }
        }
    }

    Ok(out)
}

fn load_12bit_raw(buf: &[u8], width: usize, height: usize) -> Result<Vec<u16>, DecodingError> {
    let perline = width * 12 / 8 + ((width + 2) / 10);
    let mut out = vec![0u16; width * height];

    out.chunks_exact_mut(width).enumerate().for_each(|(index, out)| {
        let inb = &buf[(index * perline)..];

        for (oc, ic) in out.chunks_exact_mut(10).zip(inb.chunks_exact(16)) {
            for (o, i) in oc.chunks_exact_mut(2).zip(ic.chunks_exact(3)) {
                let g1: u16 = i[0] as u16;
                let g2: u16 = i[1] as u16;
                let g3: u16 = i[2] as u16;

                o[0] = ((g2 & 0x0f) << 8) | g1;
                o[1] = (g3 << 4) | (g2 >> 4);
            }
        }
    });

    Ok(out)
}

